import numpy as np

myList = [1,2,3,4,5]

#converting  a python list to a numpy array

numpyArray = np.array(myList)

print(numpyArray)

#converting a multi-dimensional list to a numpy array

mySecondList = [[1,2,3],[2,3,4],[3,4,5]]

secondNumpyArray = np.array(mySecondList)

print(secondNumpyArray)